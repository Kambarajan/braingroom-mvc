public class TopSellingClassData {


    private String pic_name;
    private String class_topic;
    private String price;
    private String class_type_data;
    private String rating;
    private String class_duration;
    private String no_of_sessions;


    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getClass_topic() {
        return class_topic;
    }

    public void setClass_topic(String class_topic) {
        this.class_topic = class_topic;
    }

    public String getClass_duration() {
        return class_duration;
    }

    public void setClass_duration(String class_duration) {
        this.class_duration = class_duration;
    }

    public String getNo_of_sessions() {
        return no_of_sessions;
    }

    public void setNo_of_sessions(String no_of_sessions) {
        this.no_of_sessions = no_of_sessions;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getPic_name() {
        return pic_name;
    }

    public void setPic_name(String pic_name) {
        this.pic_name = pic_name;
    }

    public String getClass_type_data() {
        return class_type_data;
    }

    public void setClass_type_data(String class_type_data) {
        this.class_type_data = class_type_data;
    }
}
